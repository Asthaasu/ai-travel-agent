import os
from serpapi import GoogleSearch
from langchain_core.tools import tool

@tool
def search_flights(query: str) -> str:
    """Search for flights based on the user's travel request.
    Input should be a natural language query like 'flights from Delhi to Amsterdam October 1 to 7'
    """
    try:
        search = GoogleSearch({
            "q": f"flights {query}",
            "api_key": os.getenv("SERPAPI_API_KEY"),
            "engine": "google",
            "num": 5
        })
        results = search.get_dict()
        organic = results.get("organic_results", [])
        if not organic:
            return "No flight results found. Try a different query."
        
        output = "✈️ Flight Options Found:\n\n"
        for i, r in enumerate(organic[:4], 1):
            output += f"{i}. {r.get('title', 'N/A')}\n"
            output += f"   {r.get('snippet', '')}\n"
            output += f"   Link: {r.get('link', '')}\n\n"
        return output
    except Exception as e:
        return f"Error searching flights: {str(e)}"


